package com.example.skatert;

public class Genres {
    public boolean hipHop;
    public boolean rock;
    public boolean rap;
    public boolean pop;
    public boolean alternative;
    public boolean classic;
    public boolean edm;

    public boolean isHipHop() {
        return hipHop;
    }

    public void setHipHop(boolean hipHop) {
        this.hipHop = hipHop;
    }

    public boolean isRock() {
        return rock;
    }

    public void setRock(boolean rock) {
        this.rock = rock;
    }

    public boolean isRap() {
        return rap;
    }

    public void setRap(boolean rap) {
        this.rap = rap;
    }

    public boolean isPop() {
        return pop;
    }

    public void setPop(boolean pop) {
        this.pop = pop;
    }

    public boolean isAlternative() {
        return alternative;
    }

    public void setAlternative(boolean alternative) {
        this.alternative = alternative;
    }

    public boolean isClassic() {
        return classic;
    }

    public void setClassic(boolean classic) {
        this.classic = classic;
    }

    public boolean isEdm() {
        return edm;
    }

    public void setEdm(boolean edm) {
        this.edm = edm;
    }
}
